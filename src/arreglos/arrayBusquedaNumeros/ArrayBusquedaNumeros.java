package arreglos.arrayBusquedaNumeros;

public class ArrayBusquedaNumeros {
    public static void main(String[] args) {

        int[] array = {1,3,5,4,3,23,4};
        int cont = 0;
        for (int i = 0 ; i < array.length ; i ++){
            cont++;
        }
        System.out.println(cont);

    }
}
